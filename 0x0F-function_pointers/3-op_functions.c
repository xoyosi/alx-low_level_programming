#include "3-calc.h"

/**
 * op_add - addition
 *
 * @a: the first number
 *
 * @b: the second number
 *
 * Return: the sum
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_add - subtruction
 *
 * @a: the first number
 *
 * @b: the second number
 *
 * Return: the difference
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiplication
 *
 * @a: the first number
 *
 * @b: the second number
 *
 * Return: the product
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - division
 *
 * @a: the first number
 *
 * @b: the second number
 *
 * Return: the result of division
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - module
 *
 * @a: the first number
 *
 * @b: the second number
 *
 * Return: the remainder
 */

int op_mod(int a, int b)
{
	return (a % b);
}
